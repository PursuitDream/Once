package com.rent.po;

public class CarPO {
	
	private int bid;
	private int tid;
	private int sid;
	private String land_sign;
	private String hk_sign;
	private String color;
	public int getBid() {
		return bid;
	}
	public void setBid(int bid) {
		this.bid = bid;
	}
	public int getTid() {
		return tid;
	}
	public void setTid(int tid) {
		this.tid = tid;
	}
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getLand_sign() {
		return land_sign;
	}
	public void setLand_sign(String land_sign) {
		this.land_sign = land_sign;
	}
	public String getHk_sign() {
		return hk_sign;
	}
	public void setHk_sign(String hk_sign) {
		this.hk_sign = hk_sign;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	
	

}
